	<h1><span class="title"></span>MVC模式：控件类</h1>
	<p>本章介绍jecat系统的控件类的使用方法.</p>
	<p>jecat默认提供的控件主要有:</p>
		<ul>
			<li><strong>Text类</strong>,实现html中的&lt;input type='text'/> , &lt;input type='password'/> , &lt;input type='textarea'/> ,&lt;input type='hidden'/></li>
			<li><strong>CheckBtn类</strong>,实现html中的&lt;input type='radio'/> , &lt;input type='checkbox'/></li>
			<li><strong>Select类</strong>,实现html中的&lt;select></li>
			<li><strong>File类</strong>,实现html中的&lt;input type='file'/></li>
			<li><strong>Group类</strong>,在html页面中没有显示,用来把多个控件类组合成一个控件,方便完成类似用户注册时密码校验这样的功能</li>
			<li>除此之外jecat还提供RadioGroup等控件,随着jecat的完善,会提供越来越多的控件来提高程序员的编码效率.详情请查阅jecat手册</li>
		</ul>
	<p>本章将引导读者从零开始构建一个注册系统的页面,让读者以最快的速度体验jecat的MVC系统.</p>
